_phylum() {
    local i cur prev opts cmds
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${i}" in
            phylum)
                cmd="phylum"
                ;;
            
            auth)
                cmd+="__auth"
                ;;
            batch)
                cmd+="__batch"
                ;;
            cancel)
                cmd+="__cancel"
                ;;
            create)
                cmd+="__create"
                ;;
            help)
                cmd+="__help"
                ;;
            heuristics)
                cmd+="__heuristics"
                ;;
            init)
                cmd+="__init"
                ;;
            keys)
                cmd+="__keys"
                ;;
            list)
                cmd+="__list"
                ;;
            login)
                cmd+="__login"
                ;;
            ping)
                cmd+="__ping"
                ;;
            register)
                cmd+="__register"
                ;;
            remove)
                cmd+="__remove"
                ;;
            status)
                cmd+="__status"
                ;;
            submit)
                cmd+="__submit"
                ;;
            version)
                cmd+="__version"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        phylum)
            opts=" -c -t -h -V  --config --timeout --help --version  auth ping init submit batch status cancel heuristics version help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                
                --config)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                    -c)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                --timeout)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                    -t)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        
        phylum__auth)
            opts=" -h -V  --help --version  register login keys status"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
